template<typename FuncType>
inline FuncType CallVFunction(void* ppClass, DWORD index)
{
	auto pVTable = *static_cast<PDWORD*>(ppClass);
	auto dwAddress = pVTable[index];
	return reinterpret_cast<FuncType>(dwAddress);
}

